import codecs

# ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡πÉ‡∏´‡πâ‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•‡∏†‡∏≤‡∏©‡∏≤‡πÑ‡∏ó‡∏¢‡πÑ‡∏î‡πâ‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á
sys.stdout = codecs.getwriter('utf-8')(sys.stdout.buffer, errors='replace')
sys.stderr = codecs.getwriter('utf-8')(sys.stderr.buffer, errors='replace')

def rename_files_clean_format(directory: Path, exts: Set[str], dry_run: bool = True) -> int:
    """
    Logic ‡πÉ‡∏´‡∏°‡πà:
    1. ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏ó‡∏µ‡πà‡∏™‡∏∞‡∏≠‡∏≤‡∏î (‡∏ï‡∏±‡∏î _jpg, ‡∏ï‡∏±‡∏î .rf, ‡∏ï‡∏±‡∏î‡∏™‡πà‡∏ß‡∏ô‡πÄ‡∏Å‡∏¥‡∏ô)
    2. ‡πÄ‡∏ä‡πá‡∏Ñ‡∏ß‡πà‡∏≤‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà‡∏à‡∏£‡∏¥‡∏á‡πÑ‡∏´‡∏°?
       - ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß -> ‡∏•‡∏ö‡πÑ‡∏ü‡∏•‡πå‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô‡∏ó‡∏¥‡πâ‡∏á (‡πÄ‡∏û‡∏£‡∏≤‡∏∞‡∏°‡∏±‡∏ô‡∏Ñ‡∏∑‡∏≠‡πÑ‡∏ü‡∏•‡πå‡∏ã‡πâ‡∏≥‡∏ó‡∏µ‡πà‡∏ä‡∏∑‡πà‡∏≠‡∏ú‡∏¥‡∏î)
       - ‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ -> ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô‡πÄ‡∏õ‡πá‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢
    """
    renamed_count = 0
    deleted_count = 0
    
    # Regex ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏à‡∏±‡∏ö pattern (‡πÄ‡∏ú‡∏∑‡πà‡∏≠‡∏Å‡∏£‡∏ì‡∏µ‡∏ä‡∏∑‡πà‡∏≠‡∏¢‡∏≤‡∏ß‡πÜ)
    shorten_pattern = re.compile(r'^(\d+)_.+?_(\d{8}_\d{6}_panorama.*)$')

    files = list(directory.iterdir())
    
    print(f"‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö {len(files)} ‡πÑ‡∏ü‡∏•‡πå...")

    for p in files:
        if not p.is_file(): continue
        
        # ‡∏Å‡∏£‡∏≠‡∏á‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏ô‡∏≤‡∏°‡∏™‡∏Å‡∏∏‡∏•‡∏ó‡∏µ‡πà‡∏Å‡∏≥‡∏´‡∏ô‡∏î ‡∏´‡∏£‡∏∑‡∏≠‡∏ñ‡πâ‡∏≤‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå‡∏°‡∏±‡∏ô‡∏°‡∏µ .rf. ‡∏Å‡πá‡πÉ‡∏´‡πâ‡πÄ‡∏Ç‡πâ‡∏≤‡∏°‡∏≤‡πÅ‡∏Å‡πâ‡∏î‡πâ‡∏ß‡∏¢
        if p.suffix.lower().lstrip('.') not in exts and ".rf." not in p.name:
            continue

        original_name = p.name
        new_name = original_name

        # -------------------------------------------------
        # Step 1: ‡∏•‡∏ö _jpg.txt ‡∏´‡∏£‡∏∑‡∏≠ _png.txt ‡∏≠‡∏≠‡∏Å
        # -------------------------------------------------
        if new_name.endswith("_jpg.txt"):
            new_name = new_name.replace("_jpg.txt", ".txt")
        elif new_name.endswith("_png.txt"):
            new_name = new_name.replace("_png.txt", ".txt")

        # -------------------------------------------------
        # Step 2: ‡∏ï‡∏±‡∏î .rf. ‡∏ó‡∏¥‡πâ‡∏á
        # -------------------------------------------------
        if ".rf." in new_name:
            # ‡∏ï‡∏±‡∏î‡πÄ‡∏≠‡∏≤‡πÅ‡∏Ñ‡πà‡∏™‡πà‡∏ß‡∏ô‡∏´‡∏ô‡πâ‡∏≤ .rf.
            base_part = new_name.split(".rf.")[0]
            # ‡πÄ‡∏ï‡∏¥‡∏°‡∏ô‡∏≤‡∏°‡∏™‡∏Å‡∏∏‡∏• .txt ‡∏Å‡∏•‡∏±‡∏ö‡πÄ‡∏Ç‡πâ‡∏≤‡πÑ‡∏õ
            new_name = base_part + ".txt"

        # -------------------------------------------------
        # Step 3: ‡∏ï‡∏±‡∏î‡∏™‡πà‡∏ß‡∏ô‡πÄ‡∏Å‡∏¥‡∏ô‡∏ï‡∏£‡∏á‡∏Å‡∏•‡∏≤‡∏á (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ pattern ‡∏ô‡∏±‡πâ‡∏ô)
        # -------------------------------------------------
        # ‡πÄ‡∏ä‡πá‡∏Ñ‡∏ß‡πà‡∏≤‡∏ä‡∏∑‡πà‡∏≠‡∏ï‡∏≠‡∏ô‡∏ô‡∏µ‡πâ‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ö pattern ‡∏¢‡∏≤‡∏ß‡πÜ ‡πÑ‡∏´‡∏°
        match = shorten_pattern.match(new_name)
        if match:
            # ‡∏à‡∏±‡∏î format ‡πÉ‡∏´‡∏°‡πà‡πÄ‡∏õ‡πá‡∏ô: ‡πÄ‡∏•‡∏Ç‡∏´‡∏ô‡πâ‡∏≤_‡∏ß‡∏±‡∏ô‡πÄ‡∏ß‡∏•‡∏≤...
            new_name = f"{match.group(1)}_{match.group(2)}"

        # -------------------------------------------------
        # Step 4: ‡∏î‡∏≥‡πÄ‡∏ô‡∏¥‡∏ô‡∏Å‡∏≤‡∏£ (‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠ ‡∏´‡∏£‡∏∑‡∏≠ ‡∏•‡∏ö)
        # -------------------------------------------------
        
        # ‡∏ñ‡πâ‡∏≤‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÅ‡∏õ‡∏•‡∏á (‡∏´‡∏°‡∏≤‡∏¢‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ß‡πà‡∏≤‡πÑ‡∏ü‡∏•‡πå‡∏ô‡∏µ‡πâ‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏°‡πà clean)
        if new_name != original_name:
            new_path = p.parent / new_name
            
            # ‡∏Å‡∏£‡∏ì‡∏µ‡∏ó‡∏µ‡πà 1: ‡πÑ‡∏ü‡∏•‡πå‡∏õ‡∏•‡∏≤‡∏¢‡∏ó‡∏≤‡∏á‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß (Duplicate)
            # ‡πÄ‡∏ä‡πà‡∏ô‡∏°‡∏µ‡∏ó‡∏±‡πâ‡∏á image.txt ‡πÅ‡∏•‡∏∞ image_jpg.txt -> ‡πÄ‡∏£‡∏≤‡∏à‡∏∞‡∏•‡∏ö image_jpg.txt ‡∏ó‡∏¥‡πâ‡∏á
            if new_path.exists():
                if dry_run:
                    print(f" [Dry-Run] üóë ‡∏à‡∏∞‡∏•‡∏ö‡πÑ‡∏ü‡∏•‡πå‡∏ã‡πâ‡∏≥: {original_name} (‡πÄ‡∏û‡∏£‡∏≤‡∏∞‡∏°‡∏µ {new_name} ‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß)")
                else:
                    try:
                        p.unlink() # ‡∏Ñ‡∏≥‡∏™‡∏±‡πà‡∏á‡∏•‡∏ö‡πÑ‡∏ü‡∏•‡πå
                        print(f" üóë ‡∏•‡∏ö‡πÑ‡∏ü‡∏•‡πå‡∏ã‡πâ‡∏≥: {original_name}")
                        deleted_count += 1
                    except Exception as e:
                        print(f" ‚úó ‡∏•‡∏ö‡πÑ‡∏°‡πà‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à {original_name}: {e}")

            # ‡∏Å‡∏£‡∏ì‡∏µ‡∏ó‡∏µ‡πà 2: ‡πÑ‡∏ü‡∏•‡πå‡∏õ‡∏•‡∏≤‡∏¢‡∏ó‡∏≤‡∏á‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ -> ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡∏ï‡∏≤‡∏°‡∏õ‡∏Å‡∏ï‡∏¥
            else:
                if dry_run:
                    print(f" [Dry-Run] ‚úè ‡∏à‡∏∞‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠: {original_name} --> {new_name}")
                else:
                    try:
                        p.rename(new_path)
                        print(f" ‚úì ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠: {original_name} --> {new_name}")
                        renamed_count += 1
                    except Exception as e:
                        print(f" ‚úó ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏°‡πà‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à {original_name}: {e}")

    return renamed_count

def main() -> None:
    parser = argparse.ArgumentParser(description="Clean Labels and Remove Duplicates")
    
    # Path ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô
    parser.add_argument("--dir", default=r'D:\dele\train\labels', help="‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢")
    parser.add_argument("--exts", default='txt', help="‡∏ô‡∏≤‡∏°‡∏™‡∏Å‡∏∏‡∏•‡πÑ‡∏ü‡∏•‡πå")
    
    # ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡πÉ‡∏™‡πà --yes ‡∏à‡∏∞‡πÄ‡∏õ‡πá‡∏ô‡πÅ‡∏Ñ‡πà‡∏Å‡∏≤‡∏£‡∏à‡∏≥‡∏•‡∏≠‡∏á (Dry run) ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏õ‡∏•‡∏≠‡∏î‡∏†‡∏±‡∏¢
    parser.add_argument("--yes", action='store_true', help="‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô‡∏ó‡∏≥‡∏à‡∏£‡∏¥‡∏á (‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡πÉ‡∏™‡πà‡∏à‡∏∞‡πÄ‡∏õ‡πá‡∏ô‡πÅ‡∏Ñ‡πà Preview)")

    args = parser.parse_args()
    target_dir = Path(args.dir)
    exts = set([e.strip().lower() for e in args.exts.split(',') if e.strip()])

    if not target_dir.exists():
        print(f"Error: ‡πÑ‡∏°‡πà‡∏û‡∏ö‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå: {target_dir}")
        return

    print(f"Target Folder: {target_dir}")
    
    # ‡πÄ‡∏ä‡πá‡∏Ñ‡∏ß‡πà‡∏≤‡πÄ‡∏õ‡πá‡∏ô‡∏Å‡∏≤‡∏£‡∏£‡∏±‡∏ô‡∏à‡∏£‡∏¥‡∏á‡∏´‡∏£‡∏∑‡∏≠‡∏ó‡∏î‡∏™‡∏≠‡∏ö
    is_dry_run = not args.yes 

    if is_dry_run:
        print("\n!!! ‡∏ô‡∏µ‡πà‡∏Ñ‡∏∑‡∏≠‡πÇ‡∏´‡∏°‡∏î‡∏ó‡∏î‡∏™‡∏≠‡∏ö (Dry Run) - ‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡∏•‡∏ö‡∏´‡∏£‡∏∑‡∏≠‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå‡∏à‡∏£‡∏¥‡∏á !!!")
        print("‡πÉ‡∏´‡πâ‡πÄ‡∏ï‡∏¥‡∏° --yes ‡∏ï‡πà‡∏≠‡∏ó‡πâ‡∏≤‡∏¢‡∏Ñ‡∏≥‡∏™‡∏±‡πà‡∏á‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏£‡∏±‡∏ô‡∏à‡∏£‡∏¥‡∏á\n")

    print("=" * 60)
    rename_files_clean_format(target_dir, exts, dry_run=is_dry_run)
    print("=" * 60)
    
    if is_dry_run:
        print("\n‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏î‡πâ‡∏≤‡∏ô‡∏ö‡∏ô ‡∏ñ‡πâ‡∏≤‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á‡πÉ‡∏´‡πâ‡∏£‡∏±‡∏ô‡∏Ñ‡∏≥‡∏™‡∏±‡πà‡∏á‡πÄ‡∏î‡∏¥‡∏°‡πÅ‡∏•‡πâ‡∏ß‡πÄ‡∏ï‡∏¥‡∏° --yes")

if __name__ == '__main__':
    main()